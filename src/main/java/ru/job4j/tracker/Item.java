package ru.job4j.tracker;

public class Item {
    private int id;
    private String name;

    public Item(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public String name(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int id(int id, String name) {
        this.id = id;
        this.name = name;
    }
}

